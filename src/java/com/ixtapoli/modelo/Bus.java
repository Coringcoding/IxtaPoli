package com.ixtapoli.modelo;

public class Bus {

	private Long idBus;
	private String nombre;
	
	public Bus() {
		
	}

	public Long getIdBus() {
		return idBus;
	}

	public void setIdBus(Long idBus) {
		this.idBus = idBus;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
			
}
